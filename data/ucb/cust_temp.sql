SELECT gccd.PRODUCT_NAME     AS PRODUCT_NAME0,
  gccd.BODY_SYS              AS BODY_SYS1,
  gccd.INC_TERM              AS INC_TERM2,
  gccd.NEW_FATAL_COUNT       AS NEW_FATAL_COUNT3,
  gccd.CUMM_FATAL_COUNT      AS CUMM_FATAL_COUNT4,
  gccd.NEW_SERIOUS_COUNT     AS NEW_SERIOUS_COUNT5,
  gccd.CUMM_SERIOUS_COUNT    AS CUMM_SERIOUS_COUNT6,
  gccd.NEW_STUDY_COUNT       AS NEW_STUDY_COUNT7,
  gccd.CUMM_STUDY_COUNT      AS CUMM_STUDY_COUNT8,
  gccd.NEW_SPON_COUNT        AS NEW_SPON_COUNT9,
  gccd.CUMM_SPON_COUNT       AS CUMM_SPON_COUNT10,
  gapd.PRR_VALUE             AS PRR_VALUE11,
  gccl.NEW_FATAL_CASELIST    AS NEW_FATAL_CASELIST12,
  gccl.CUMM_FATAL_CASELIST   AS CUMM_FATAL_CASELIST13,
  gccl.NEW_SERIOUS_CASELIST  AS NEW_SERIOUS_CASELIST14,
  gccl.CUMM_SERIOUS_CASELIST AS CUMM_SERIOUS_CASELIST15,
  gccl.NEW_STUDY_CASELIST    AS NEW_STUDY_CASELIST16,
  gccl.CUMM_STUDY_CASELIST   AS CUMM_STUDY_CASELIST17,
  gccl.NEW_SPON_CASELIST     AS NEW_SPON_CASELIST18,
  gccl.CUMM_SPON_CASELIST    AS CUMM_SPON_CASELIST19
FROM  GTT_AGG_NEW_CUMM_CASELIST gccl
JOIN GTT_AGG_NEW_CUMM_COUNT_DATA gccd
ON (gccl.ID       = gccd.ID
AND gccl.enterprise_id = gccd.enterprise_id )
JOIN GTT_AGG_PRR_DATA gapd
ON (gccl.ID       = gapd.ID
AND gccl.enterprise_id = gapd.enterprise_id )